//Program to convert decimal to octal and hexadecimal

#include<stdio.h>

int main()
{
    char value=012;
    printf("Decimal value of %o is %d\n",value,value);
    printf("Octal value of %o is %o\n",value,value);
    printf("Hexadecimal value of %o is %x\n",value,value);

    return 0;
}