//Program to convert decimal to octal and hexadecimal

#include<stdio.h>

int main()
{
    int value=15;
    printf("Decimal value of %d is %d\n",value,value);
    printf("Octal value of %d is %o\n",value,value);
    printf("Hexadecimal value of %d is %x\n",value,value);

    return 0;
}